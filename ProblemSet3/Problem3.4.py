duration = int(input("Enter the song duration in minutes:"))# input the song duration

if duration < 2: 
    print ("short song") #print short song if the input duration is less than 2
else:
    if duration <= 4:
        print ("Medium Song")#print medium song id the duration is bigger than 2 and less equal than 4
    else:
        print("Long Song")# print long song if the duration is bigger than 4

    
    






